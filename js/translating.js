function getParents(elem) {
	var parents = [];
	while (
		elem.parentNode &&
		elem.parentNode.nodeName.toLowerCase() != 'body'
	) {
		elem = elem.parentNode;
		parents.push(elem);
	}
	return parents;
}

function updateContent() {
	var i18n_el = document.querySelectorAll('[i18n]');
	i18n_el.forEach((child) => {
		var parents = getParents(child).reverse();
		var i18n_selector = [];
		parents.forEach((parent) => {
			i18n_parent_name = parent.getAttribute('i18n-parent');
			if (i18n_parent_name != null) {
				i18n_selector.push(i18n_parent_name);
			}
		});
		i18n_selector =
			i18n_selector.join('.') + '.' + child.getAttribute('i18n');
		child.innerHTML = i18next.t(i18n_selector);
	});
}

i18next
	.use(i18nextBrowserLanguageDetector)
	.init({
		detection: {
			order: [
				'navigator',
				'querystring',
				'cookie',
				'localStorage',
				'htmlTag',
				'path',
				'subdomain',
			],
		},
		debug: true,
		resources: resources,
	})
	.then(function (t) {
		updateContent();
	});

i18next.on('languageChanged', () => {
	updateContent();
});
